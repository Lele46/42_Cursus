#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	x;

	x = 0;
	if (!s)
		return (write(1, "(null)", 6));
	while (s[x])
	{
		write (1, &s[x], 1);
		x++;
	}
	return (x);
}
